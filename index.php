<?php

$arr = [134, 1121, 34, 121];

$result = [];
$maxLength = 0;

// Ищем максимальную длину числа
foreach ($arr as $number) {
    $length = strlen((string) $number);
    if ($length > $maxLength) {
        $maxLength = $length;
    }
}

// Строим дерево по разрядам
foreach ($arr as $number) {
    $padded = str_pad((string) $number, $maxLength, '0', STR_PAD_LEFT);

    $node = &$result;

    foreach (str_split($padded) as $digit) {
        if (!isset($node[$digit])) {
            $node[$digit] = [];
        }

        $node = &$node[$digit];
    }
    // если нужно подсчитать сколько раз одно и то же число встретилось в массиве
    // $node['_count'] = ($node['_count'] ?? 0) + 1;
    $node = true;
}

echo '<pre>';
var_export($result);
echo '</pre>';

